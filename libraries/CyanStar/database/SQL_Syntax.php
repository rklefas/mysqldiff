<?php


abstract class SQL_Syntax
{

	static function castAsArray($inputs)
	{
		if (is_array($inputs))
			return $inputs;
		else if (is_object($inputs))
			return get_object_vars($inputs);
	
		return array();
	}

	static function escape_data($data)
	{
		return "";
	}


	static function escape_object($object)
	{
		return "";
	}
	
	
	

	
}